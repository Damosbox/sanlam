// Fonction utilitaire pour obtenir les marques uniques triées
export const getUniqueBrands = (): string[] => {
  const brands = [...new Set(VEHICLES.map(v => v.brand))];
  return brands.sort((a, b) => {
    const aPriority = PRIORITY_BRANDS.indexOf(a);
    const bPriority = PRIORITY_BRANDS.indexOf(b);
    if (aPriority !== -1 && bPriority !== -1) return aPriority - bPriority;
    if (aPriority !== -1) return -1;
    if (bPriority !== -1) return 1;
    return a.localeCompare(b);
  });
};

// Fonction pour formater l'affichage des années
export const formatYearRange = (startYear: string, endYear: string): string => {
  if (!endYear || endYear === "") {
    return `${startYear} - aujourd'hui`;
  }
  return `${startYear} - ${endYear}`;
};

// Fonction pour générer les années disponibles pour un véhicule
export const getAvailableYears = (startYear: string, endYear: string): number[] => {
  const start = parseInt(startYear) || 2000;
  const end = endYear && endYear !== "" ? parseInt(endYear) : new Date().getFullYear();
  const years: number[] = [];
  for (let year = end; year >= start; year--) {
    years.push(year);
  }
  return years;
};
